# variables
# variables act as containers for data
# you can assign a value to a variable using the assignment operator (=)
# They allow us to manipulate and work with data efficiently
# They have dynamic nature as they can hold all types of data
# Variables are case sensitive, so 'a' and 'A' are treated as different variables
# Variables can be reassigned with new values

# There are three standardly used methods in naming a variable

# 1. Snake case: In this method all the characters of variable name are in 
# lower case and words are separated by underscore
# example: hello_world or user_name

# 2. Camel case: In this method the characters fo first word will be lower
# case and the first character of the second word will be upper case
# and there will be no any space,hyphen or underscore between them
# example: helloWorld or userName

# 3.Pascal case: In this method the first letter of all the words in the 
# variable name would be capital and remaining letters will be in lower 
# case and there will be no any space,hyphen or underscore between them
# example: HelloWorld or UserName

# It is recommended that if in a program we use a case suppose pascal case
# then we should use it throughout the program for all the variable names
# so that it is easy to understand and read the program

